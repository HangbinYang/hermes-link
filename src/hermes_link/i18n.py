def normalize_language(value: str | None) -> Language | None:
    if not value:
        return None
    token = value.strip().replace("_", "-").lower()
    if not token or token == "auto":
        return None
    if token.startswith("zh"):
        return "zh"
    if token.startswith("en") or token in {"c", "posix"}:
        return "en"
    return None


def _extract_accept_language(value: str | None) -> Language | None:
    if not value:
        return None
    for part in value.split(","):
        language = normalize_language(part.split(";", 1)[0])
        if language:
            return language
    return None


def resolve_language(preferred: str | None = None) -> Language:
    direct = normalize_language(preferred) or _extract_accept_language(preferred)
    if direct:
        return direct

    contextual = _language_context.get()
    if contextual:
        return contextual

    env_value = normalize_language(os.getenv(HERMES_LINK_LANG_ENV))
    if env_value:
        return env_value

    for name in ("LC_ALL", "LC_MESSAGES", "LANGUAGE", "LANG"):
        env_lang = normalize_language(os.getenv(name))
        if env_lang:
            return env_lang

    try:
        locale_value = locale.getlocale()[0]
    except Exception:
        locale_value = None
    locale_lang = normalize_language(locale_value)
    if locale_lang:
        return locale_lang

    return "en"


def push_language(preferred: str | None = None) -> Token[Language | None]:
    return _language_context.set(resolve_language(preferred))


def pop_language(token: Token[Language | None]) -> None:
    _language_context.reset(token)


def t(key: str, **kwargs: object) -> str:
    language = resolve_language()
    template = _CATALOG.get(language, {}).get(key) or _CATALOG["en"].get(key) or key
    return template.format(**kwargs)
